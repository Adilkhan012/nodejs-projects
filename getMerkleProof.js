import keccak256 from "keccak256";

const getMerkleTree = () => {
  return new MerkleTree(whitelist, keccak256, {
    hashLeaves: true,
    sortPairs: true,
  });
};

export const getProof = (account) => {
  const leaf = keccak256(account);
  const proof = getMerkleTree().getHexProof(leaf);

  return proof;
};

